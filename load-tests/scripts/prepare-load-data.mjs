#!/usr/bin/env node
import { spawnSync } from 'node:child_process';
import { createHmac, randomUUID } from 'node:crypto';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '../..');
const outDir = resolve(repoRoot, 'load-tests/out');

const cfg = {
  infra: process.env.LOAD_TEST_INFRA || 'auto',
  users: intEnv('USERS', 5000),
  stock: intEnv('STOCK', 100),
  offerId: intEnv('OFFER_ID', 900001),
  userIdStart: intEnv('USER_ID_START', 1000000),
  jwtSecret: process.env.JWT_SECRET || 'dev-only-change-me-dev-only-change-me-32bytes',
  tokenTtlSeconds: intEnv('TOKEN_TTL_SECONDS', 3600),
  saleDurationSeconds: intEnv('SALE_DURATION_SECONDS', 3600),
  retainSeconds: intEnv('FLASH_SALE_RETAIN_SECONDS', 86400),
  mysqlContainer: process.env.MYSQL_CONTAINER || 'flash-sale-platform-mysql',
  mysqlDatabase: process.env.MYSQL_DATABASE || 'flash_sale_platform',
  mysqlHost: process.env.MYSQL_HOST || '127.0.0.1',
  mysqlPort: process.env.MYSQL_PORT || '3306',
  mysqlUser: process.env.MYSQL_USERNAME || 'root',
  mysqlPassword: process.env.MYSQL_PASSWORD,
  mysqlRootPassword: process.env.MYSQL_ROOT_PASSWORD,
  redisContainer: process.env.REDIS_CONTAINER || 'flash-sale-platform-redis',
  redisHost: process.env.REDIS_HOST || '127.0.0.1',
  redisPort: process.env.REDIS_PORT || '6379',
  redisPassword: process.env.REDIS_PASSWORD || '',
  rabbitContainer: process.env.RABBITMQ_CONTAINER || 'flash-sale-platform-rabbitmq',
  rabbitManagementUrl: (process.env.RABBITMQ_MANAGEMENT_URL || 'http://localhost:15672').replace(/\/$/, ''),
  rabbitManagementUsername: process.env.RABBITMQ_MANAGEMENT_USERNAME || 'guest',
  rabbitManagementPassword: process.env.RABBITMQ_MANAGEMENT_PASSWORD || 'guest',
  rabbitVhost: process.env.RABBITMQ_VHOST || '/',
  skipRabbitPurge: boolEnv('SKIP_RABBIT_PURGE', false),
  emailDomain: process.env.LOAD_TEST_EMAIL_DOMAIN || 'loadtest.local',
  tokenFile: resolve(repoRoot, process.env.TOKEN_FILE || 'load-tests/out/tokens.json'),
  metadataFile: resolve(repoRoot, process.env.METADATA_FILE || 'load-tests/out/metadata.json'),
};

cfg.resolvedInfra = resolveInfra(cfg.infra);
if (cfg.mysqlPassword === undefined) {
  cfg.mysqlPassword = cfg.resolvedInfra === 'docker' ? (cfg.mysqlRootPassword || 'root') : '';
}

if (Buffer.byteLength(cfg.jwtSecret) < 32) {
  throw new Error('JWT_SECRET must be at least 32 bytes, matching JwtUtils in the application.');
}

mkdirSync(outDir, { recursive: true });

const nowEpoch = Math.floor(Date.now() / 1000);
const beginEpoch = nowEpoch - 60;
const endEpoch = nowEpoch + cfg.saleDurationSeconds;
const userIdEnd = cfg.userIdStart + cfg.users - 1;
const userIdentityColumn = process.env.USER_IDENTITY_COLUMN || detectUserIdentityColumn();

console.log('Preparing flash-sale load-test data...');
console.log(`infra=${cfg.resolvedInfra}, offerId=${cfg.offerId}, users=${cfg.users}, stock=${cfg.stock}`);

prepareMysql();
const tokens = prepareRedisAndTokens();
await purgeRabbitQueues();

writeFileSync(cfg.tokenFile, JSON.stringify(tokens));
writeFileSync(
  cfg.metadataFile,
  JSON.stringify(
    {
      offerId: cfg.offerId,
      users: cfg.users,
      stock: cfg.stock,
      expectedAccepted: Math.min(cfg.stock, cfg.users),
      userIdStart: cfg.userIdStart,
      userIdEnd,
      tokenFile: relativePath(cfg.tokenFile),
      beginEpoch,
      endEpoch,
      generatedAt: new Date().toISOString(),
    },
    null,
    2
  )
);

console.log(`Token file: ${relativePath(cfg.tokenFile)}`);
console.log(`Metadata:   ${relativePath(cfg.metadataFile)}`);
console.log('Next: run k6 with load-tests/k6/flash-sale-order.js');

function prepareMysql() {
  const setupSql = `
SET FOREIGN_KEY_CHECKS = 0;
DELETE FROM payment_webhook_event WHERE order_id IN (SELECT id FROM orders WHERE offer_id = ${cfg.offerId});
DELETE FROM payment_order WHERE order_id IN (SELECT id FROM orders WHERE offer_id = ${cfg.offerId});
DELETE FROM orders WHERE offer_id = ${cfg.offerId};
INSERT INTO offers (
  id, merchant_id, title, description, rules, price_amount, face_value_amount, status, create_time, update_time
) VALUES (
  ${cfg.offerId}, 1, 'Load Test Flash Sale Offer', 'Generated by load-tests/scripts/prepare-load-data.mjs',
  'Dedicated offer for load testing. Do not use for real orders.', 475, 1000, 1, NOW(), NOW()
) ON DUPLICATE KEY UPDATE
  merchant_id = VALUES(merchant_id),
  title = VALUES(title),
  description = VALUES(description),
  rules = VALUES(rules),
  price_amount = VALUES(price_amount),
  face_value_amount = VALUES(face_value_amount),
  status = VALUES(status),
  update_time = NOW();
INSERT INTO flash_sale_offers (
  offer_id, stock, create_time, begin_time, end_time, update_time
) VALUES (
  ${cfg.offerId}, ${cfg.stock}, NOW(), FROM_UNIXTIME(${beginEpoch}), FROM_UNIXTIME(${endEpoch}), NOW()
) ON DUPLICATE KEY UPDATE
  stock = VALUES(stock),
  begin_time = VALUES(begin_time),
  end_time = VALUES(end_time),
  update_time = NOW();
SET FOREIGN_KEY_CHECKS = 1;
`;
  mysql(setupSql);

  const batchSize = 1000;
  for (let offset = 0; offset < cfg.users; offset += batchSize) {
    const rows = [];
    const end = Math.min(offset + batchSize, cfg.users);
    for (let i = offset; i < end; i += 1) {
      const userId = cfg.userIdStart + i;
      const nickName = `LoadUser_${userId}`;
      const identity = `loadtest+${cfg.offerId}+${userId}@${cfg.emailDomain}`;
      rows.push(
        `(${userId}, ${sqlString(identity)}, '', ${sqlString(nickName)}, '', 'USER', 'ACTIVE', NOW(), NOW())`
      );
    }

    mysql(`
INSERT INTO tb_user (
  id, ${userIdentityColumn}, password, nick_name, icon, role, status, create_time, update_time
) VALUES
${rows.join(',\n')}
ON DUPLICATE KEY UPDATE
  ${userIdentityColumn} = VALUES(${userIdentityColumn}),
  nick_name = VALUES(nick_name),
  role = 'USER',
  status = 'ACTIVE',
  update_time = NOW();
`);
  }
}

function prepareRedisAndTokens() {
  const warmupCommands = [
    ['DEL', stockKey(), orderKey(), offerKey()],
    ['SET', stockKey(), String(cfg.stock)],
    ['HSET', offerKey(), 'begin', String(beginEpoch), 'end', String(endEpoch), 'retainSeconds', String(cfg.retainSeconds)],
    ['EXPIREAT', stockKey(), String(endEpoch + cfg.retainSeconds)],
    ['EXPIREAT', offerKey(), String(endEpoch + cfg.retainSeconds)],
    ['EXPIREAT', orderKey(), String(endEpoch + cfg.retainSeconds)],
  ];
  redisPipe(warmupCommands);

  const tokens = [];
  const commandBatch = [];
  const redisBatchCommandLimit = 4000;
  for (let i = 0; i < cfg.users; i += 1) {
    const userId = cfg.userIdStart + i;
    const sid = randomUUID();
    const jti = randomUUID();
    const nickName = `LoadUser_${userId}`;
    const token = signJwt({
      sub: nickName,
      iat: nowEpoch,
      exp: nowEpoch + cfg.tokenTtlSeconds,
      jti,
      userId,
      role: 'USER',
      sid,
    });

    tokens.push({ userId, token });
    commandBatch.push(['HSET', `login:token:${jti}`, 'id', String(userId), 'nickName', nickName, 'icon', '', 'role', 'USER']);
    commandBatch.push(['EXPIRE', `login:token:${jti}`, String(cfg.tokenTtlSeconds)]);

    if (commandBatch.length >= redisBatchCommandLimit) {
      redisPipe(commandBatch.splice(0, commandBatch.length));
    }
  }

  if (commandBatch.length > 0) {
    redisPipe(commandBatch);
  }

  return tokens;
}

function signJwt(payload) {
  const header = base64Url(JSON.stringify({ alg: 'HS256', typ: 'JWT' }));
  const body = base64Url(JSON.stringify(payload));
  const unsigned = `${header}.${body}`;
  const signature = createHmac('sha256', Buffer.from(cfg.jwtSecret, 'utf8'))
    .update(unsigned)
    .digest('base64url');
  return `${unsigned}.${signature}`;
}

function mysql(sql) {
  mysqlOutput(sql);
}

function mysqlOutput(sql) {
  if (cfg.resolvedInfra === 'docker') {
    return run(
      'docker',
      ['exec', '-i', cfg.mysqlContainer, 'mysql', '-uroot', `-p${cfg.mysqlPassword}`, cfg.mysqlDatabase],
      sql
    );
  }

  return run('mysql', mysqlArgs(), sql);
}

function redisPipe(commands) {
  if (cfg.resolvedInfra === 'docker') {
    run('docker', ['exec', '-i', cfg.redisContainer, 'redis-cli', '--pipe'], commands.map(respCommand).join(''));
    return;
  }

  run('redis-cli', redisArgs('--pipe'), commands.map(respCommand).join(''));
}

async function purgeRabbitQueues() {
  if (cfg.skipRabbitPurge) {
    console.log('RabbitMQ queue purge skipped.');
    return;
  }

  const queues = [
    'flashsale.order.create.queue',
    'flashsale.order.create.retry.queue',
    'flashsale.order.create.dlq',
  ];

  if (cfg.resolvedInfra === 'docker') {
    for (const queue of queues) {
      runOptional('docker', ['exec', cfg.rabbitContainer, 'rabbitmqctl', 'purge_queue', queue]);
    }
    return;
  }

  const encodedVhost = encodeURIComponent(cfg.rabbitVhost);
  const auth = Buffer.from(`${cfg.rabbitManagementUsername}:${cfg.rabbitManagementPassword}`).toString('base64');
  for (const queue of queues) {
    const response = await fetch(
      `${cfg.rabbitManagementUrl}/api/queues/${encodedVhost}/${encodeURIComponent(queue)}/contents`,
      {
        method: 'DELETE',
        headers: {
          Authorization: `Basic ${auth}`,
        },
      }
    );
    if (!response.ok && response.status !== 404) {
      throw new Error(`Failed to purge RabbitMQ queue ${queue}: HTTP ${response.status}`);
    }
  }
}

function run(command, args, input) {
  const result = spawnSync(command, args, {
    input,
    encoding: 'utf8',
    maxBuffer: 1024 * 1024 * 50,
  });
  if (result.status !== 0) {
    throw new Error(
      [
        `Command failed: ${command} ${args.join(' ')}`,
        result.stdout && `stdout:\n${result.stdout}`,
        result.stderr && `stderr:\n${result.stderr}`,
      ]
        .filter(Boolean)
        .join('\n')
    );
  }
  return result.stdout;
}

function runOptional(command, args, input) {
  spawnSync(command, args, {
    input,
    encoding: 'utf8',
    maxBuffer: 1024 * 1024 * 10,
  });
}

function respCommand(args) {
  let output = `*${args.length}\r\n`;
  for (const arg of args) {
    const value = String(arg);
    output += `$${Buffer.byteLength(value)}\r\n${value}\r\n`;
  }
  return output;
}

function base64Url(value) {
  return Buffer.from(value).toString('base64url');
}

function stockKey() {
  return `flashsale:stock:${cfg.offerId}`;
}

function orderKey() {
  return `flashsale:order:${cfg.offerId}`;
}

function offerKey() {
  return `flashsale:offer:${cfg.offerId}`;
}

function sqlString(value) {
  return `'${String(value).replace(/\\/g, '\\\\').replace(/'/g, "''")}'`;
}

function intEnv(name, fallback) {
  const value = process.env[name];
  if (value === undefined || value === '') {
    return fallback;
  }
  const parsed = Number(value);
  if (!Number.isInteger(parsed) || parsed < 0) {
    throw new Error(`${name} must be a non-negative integer.`);
  }
  return parsed;
}

function boolEnv(name, fallback) {
  const value = process.env[name];
  if (value === undefined || value === '') {
    return fallback;
  }
  return ['1', 'true', 'yes', 'y'].includes(value.toLowerCase());
}

function relativePath(file) {
  return file.replace(`${repoRoot}/`, '');
}

function detectUserIdentityColumn() {
  const output = mysqlOutput(`
SELECT column_name
FROM information_schema.columns
WHERE table_schema = DATABASE()
  AND table_name = 'tb_user'
  AND column_name IN ('email', 'phone')
ORDER BY FIELD(column_name, 'email', 'phone')
LIMIT 1;
`);
  const column = output.trim().split(/\s+/).pop();
  if (column !== 'email' && column !== 'phone') {
    throw new Error("Could not detect tb_user identity column. Expected either 'email' or 'phone'.");
  }
  return column;
}

function resolveInfra(value) {
  if (value === 'docker' || value === 'local') {
    return value;
  }
  if (value !== 'auto') {
    throw new Error('LOAD_TEST_INFRA must be one of: auto, docker, local.');
  }
  return commandExists('docker') ? 'docker' : 'local';
}

function commandExists(command) {
  const result = spawnSync('sh', ['-c', `command -v ${command}`], {
    encoding: 'utf8',
  });
  return result.status === 0;
}

function mysqlArgs() {
  const args = ['-h', cfg.mysqlHost, '-P', cfg.mysqlPort, '-u', cfg.mysqlUser];
  if (cfg.mysqlPassword) {
    args.push(`-p${cfg.mysqlPassword}`);
  }
  args.push(cfg.mysqlDatabase);
  return args;
}

function redisArgs(...extraArgs) {
  const args = ['-h', cfg.redisHost, '-p', cfg.redisPort];
  if (cfg.redisPassword) {
    args.push('-a', cfg.redisPassword);
  }
  return [...args, ...extraArgs];
}
